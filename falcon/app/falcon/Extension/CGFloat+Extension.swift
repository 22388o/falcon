import UIKit

extension CGFloat {

    // Use as top and bottom margin for rows in receive settings and other views
    static let verticalRowMargin: CGFloat = 12.0

    // Use as top margin for bottom drawer like views
    static let bottomDrawerTopMargin: CGFloat = 20.0

    // Use as leading and trailing constant for almost all the views
    static let sideMargin: CGFloat = 16.0

}
